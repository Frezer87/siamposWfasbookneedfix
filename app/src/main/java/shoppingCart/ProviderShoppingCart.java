package shoppingCart;

 public class ProviderShoppingCart {
    private String name;
    private String amount;
    private String price;
    private String singlePrices;
    private String id;
    private String special;
    private String imageUrl;
    private String spicy;
    private String option;
    private String optionsList;
    private String description;
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSpecial() {
        return special;
    }

    public void setSpecial(String special) {
        this.special = special;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

     public String getSpicy() {
         return spicy;
     }

     public String getSinglePrices() {
         return singlePrices;
     }

     public void setSinglePrices(String singlePrices) {
         this.singlePrices = singlePrices;
     }

     public void setSpicy(String spicy) {
         this.spicy = spicy;
     }

     public String getOption() {
         return option;
     }

     public void setOption(String option) {
         this.option = option;
     }



     public String getDescription() {
         return description;
     }

     public void setDescription(String description) {
         this.description = description;
     }


     public String getOptionsList() {
         return optionsList;
     }

     public void setOptionsList(String optionsList) {
         this.optionsList = optionsList;
     }

 }
